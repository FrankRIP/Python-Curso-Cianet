import math

#pow eleva un numero a un exponente
print (math.pow(10, 2))          #cuando pongo math la estoy invocando
#sqrt es para sacar la raiz cuadrada de un numero
print (math.sqrt (64))
#isqrt es para sacar la raiz cudrada de un numero en entero
print (math.isqrt (64))
#sin es para sacar el seno de un numero
print (math.sin (90))
#cos es para sacar el coseno de un numero
print (math.cos (90))
#tan es para sacar la tangente de un numero
print (math.tan (90))
#factorial que es para sacar el factorial del numero
print (math.factorial (64))

import random

#randint toma un segmento de un rango de numeros y muestra un numero al azar
