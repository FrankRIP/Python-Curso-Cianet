cadena = "Estoy mostrando los metodos booleanos para las Strings"

#startswwith verifica si es verdadero o falso que la cadena inicia con la letra "E"
print(cadena.startswith("E"))

#endswith verifica su la cadena termina en "s"
print(cadena.endswith("s"))

#isupper verifica si toda la cadena de texto esta en mayuscula
print(cadena.isupper())

#islower verifica si esta en minuscula toda la cadena
print(cadena.islower())