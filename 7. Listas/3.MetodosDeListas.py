lista = [1, 2, 3, 4, 5]

print (lista)

#append es para agregar un elemento nuevo a la lista, entre el parentesis el nuevo elemento
#en este caso "6"
lista.append(6)
print(lista)

#cuando son numeros los valores se agregan sin comillas
#cuando es string se ponen comillas
lista.append('Python')
print(lista)

#insert es para inserta un nuevo valor en un espacio especifico
#en este caso "2" es el espacio donde pondre el valor y "2.5" es el valor que pondre
lista.insert(2, 2.5)
print(lista)